#ifndef __CALCULTAOT_H_
#define __CALCULTAOT_H_
#include "lpc_types.h"


/*********************************************************************************************************
1字节2位BCD数据转换成整数
**********************************************************************************************************/
extern uint8_t BCD_To_HEX(uint8_t bcd);

/*********************************************************************************************************
1字节整数转换成BCD数据
*********************************************************************************************************/
extern uint8_t HEX_To_BCD(uint8_t hex);

/*********************************************************************************************************
N字节2位BCD数组转换成 1字节32位整数(BCD数组低位在前)
*********************************************************************************************************/
extern unsigned int NBCD_To_HEX_L(unsigned char *dbuf,unsigned int len);
/***********************************
type ０代表数据　１代表是密码
len　数据长度
dotcnt　小数点位数返回
************************************/
extern uint32_t ASCII_To_HEX(uint8_t *data,uint8_t len,uint8_t dot);

/*********************************************************************************************************
** Function name:           CheckSum	   
** Descriptions:            系统参数初始化	
** input parameters:        *start	:	指向数据的指针
**			                  	*end	:	计算的数据的尾指针
** output parameters:       无      
** Returned value:          无 
*********************************************************************************************************/
extern uint8_t	CheckSum(uint8_t *start, uint8_t *end);


/*********************************************************************************************************
ACS数据帧转换成HEX数组(限BCD码)
*********************************************************************************************************/
extern void AscToHexStr(uint8_t *ASCbuf,uint8_t *HEXbuf,uint8_t len);

/*********************************************************************************************************
HEX数组转换成ACS数据帧(限BCD数字码)
*********************************************************************************************************/
extern void HexToAscStr(uint8_t *HEXbuf,uint8_t *ASCbuf,uint8_t len);


#endif
